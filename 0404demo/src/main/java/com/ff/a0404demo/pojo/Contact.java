package com.ff.a0404demo.pojo;

public class Contact {

    private int img;
    private String name;


    public Contact(int img, String name) {
        this.img = img;
        this.name = name;

    }

    public int getImg() {
        return this.img;
    }

    public String getName() {
        return this.name;
    }
}
